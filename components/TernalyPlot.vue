<template>
  <div>
    <v-container grid-list-md>
      <v-row>
        <v-col md="6">
          <v-card outlined>
            <v-data-table
              :headers="headers"
              :items="dataSets"
              :items-per-page="10"
              class="elevation-1"
            >
              <template v-slot:top>
                <v-toolbar flat color="white">
                  <v-dialog v-model="dialog" max-width="500px">
                    <template v-slot:activator="{ on, attrs }">
                      <v-btn
                        color="primary"
                        dark
                        class="mb-2"
                        v-bind="attrs"
                        v-on="on"
                        >Add Points</v-btn
                      >
                    </template>
                    <v-card>
                      <v-card-title>
                        <span class="headline">{{ formTitle }}</span>
                      </v-card-title>

                      <v-card-text>
                        <v-container>
                          <v-row>
                            <v-col cols="12" sm="6" md="4">
                              <v-text-field
                                v-model="editedItem.label"
                                label="label name"
                              ></v-text-field>
                            </v-col>
                            <v-col cols="12" sm="6" md="4">
                              <v-text-field
                                v-model="editedItem.Al"
                                label="Al at.%"
                              ></v-text-field>
                            </v-col>
                            <v-col cols="12" sm="6" md="4">
                              <v-text-field
                                v-model="editedItem.Pd"
                                label="Pd at.%"
                              ></v-text-field>
                            </v-col>
                            <v-col cols="12" sm="6" md="4">
                              <v-text-field
                                v-model="editedItem.Ru"
                                label="Ru"
                              ></v-text-field>
                            </v-col>
                            <v-col cols="12" sm="6" md="4">
                              {{
                                Number(editedItem.Al) +
                                  Number(editedItem.Pd) +
                                  Number(editedItem.Ru)
                              }}
                            </v-col>
                          </v-row>
                        </v-container>
                      </v-card-text>

                      <v-card-actions>
                        <v-spacer></v-spacer>
                        <v-btn color="blue darken-1" text @click="close"
                          >Cancel</v-btn
                        >
                        <v-btn color="blue darken-1" text @click="save"
                          >Save</v-btn
                        >
                      </v-card-actions>
                    </v-card>
                  </v-dialog>
                </v-toolbar>
              </template>
              <template v-slot:item.actions="{ item }">
                <v-icon small class="mr-2" @click="editItem(item)">
                  mdi-pencil
                </v-icon>
                <v-icon small @click="deleteItem(item)">
                  mdi-delete
                </v-icon>
              </template>
              <template v-slot:no-data>
                <v-btn color="primary" @click="initialize">Reset</v-btn>
              </template>
            </v-data-table>
          </v-card>
        </v-col>
        <v-col outlined md="6" cols="12">
          <v-card outlined>
            <div id="ternaly" ref="ternaly"></div>
          </v-card>
        </v-col>
      </v-row>
    </v-container>
    <v-select
      v-model="imgSelect"
      :items="items"
      item-text="title"
      item-value="src"
      label="Select"
      return-object
      single-line
    ></v-select>
    <v-slider
      v-model="imgXpoint"
      min="0"
      max="1"
      step="0.001"
      :label="`X: ${imgXpoint}`"
    ></v-slider>
    <v-slider
      v-model="imgYpoint"
      min="0"
      max="3"
      step="0.001"
      :label="`Y: ${imgYpoint}`"
    ></v-slider>
    <v-slider
      v-model="imgSize"
      min="0"
      max="3"
      step="0.01"
      :label="`SIZE: ${imgSize}`"
    ></v-slider>
    <v-slider
      v-model="imgOpacity"
      min="0"
      max="1"
      step="0.01"
      :label="`OPACITY: ${imgOpacity}`"
    ></v-slider>
  </div>
</template>

<script lang="ts">
import { Vue, Component, Watch } from 'nuxt-property-decorator'
import * as Plotly from 'plotly.js-dist'
import imageDefinition from '@/plugins/imageDefinition'

interface Select {
  title: string
  src: string
  x: number
  y: number
  size: number
}

@Component
export default class TernalyPlot extends Vue {
  editedItem: {
    Al: number
    Ru: number
    Pd: number
    label: string
  } = { Al: 0, Ru: 0, Pd: 0, label: '' }

  defaultItem: {
    Al: number
    Ru: number
    Pd: number
    label: string
  } = { Al: 0, Ru: 0, Pd: 0, label: '' }

  dataSets: any = []
  editedIndex: number = -1
  dialog: boolean = false

  imgXpoint: number = 0.379
  imgYpoint: number = 0.79
  imgSize: number = 0.86
  imgOpacity: number = 1
  items: object = imageDefinition
  imgSelect: Select = { ...imageDefinition[0] }
  rawData: {
    Al: number
    Ru: number
    Pd: number
    label: string
  }[] = [
    { Al: 72.2, Ru: 13.9, Pd: 13.9, label: 'P40<->C' },
    // { Al: 71.4, Ru: 11.9, Pd: 16.7, label: 'P40 calc.' },
    { Al: 72.0, Ru: 11.6, Pd: 16.4, label: 'P40 obs.' }
    // { Al: 71.0, Ru: 10, Pd: 19, label: '1/2 obs..' },
    // { Al: 72.36, Ru: 11.97, Pd: 15.67, label: 'x = 0' },
    // { Al: 71.26, Ru: 12.21, Pd: 16.53, label: 'x = 10' },
    // { Al: 70.44, Ru: 12.84, Pd: 16.72, label: 'x= 15' },
    // { Al: 71.95, Ru: 12.49, Pd: 15.56, label: 'x= 20' },
    // { Al: 67.2, Ru: 8.4, Pd: 24.4, label: 'P20 epma' },
    // { Al: 70.0, Ru: 7.7, Pd: 22.3, label: 'P20 ref.' },
    // { Al: 70.0, Ru: 9.0, Pd: 21.0, label: 'P20' },
    // { Al: 70, Ru: 20, Pd: 10, label: 'tsai I phase' },
    // { Al: 72, Ru: 3, Pd: 25, label: 'tsai D phase' },
    // { Al: 70, Ru: 20, Pd: 10, label: 'tsai 1/2 phase' },
    // { Al: 75, Ru: 5, Pd: 20, label: 'tsai D + FCi' },
    // { Al: 75, Ru: 10, Pd: 15, label: 'tsai D phase' },
    // { Al: 75, Ru: 15, Pd: 10, label: 'tsai D phase' }

    // { Al: 70.4, Ru: 14.9, Pd: 14.7, label: 'C1 epma' },
    // { Al: 71.2, Ru: 10.8, Pd: 18.0, label: 'N13 48h' },
    // { Al: 71.1, Ru: 11.1, Pd: 17.8, label: 'N13 120h' }
  ]

  headers: {
    text: string
    align: string
    sortable: boolean
    value: string
  }[] = [
    { text: 'label', align: 'start', sortable: true, value: 'label' },
    { text: 'Al at.%', align: 'center', sortable: true, value: 'Al' },
    { text: 'Pd at.%', align: 'center', sortable: true, value: 'Pd' },
    { text: 'Ru at.%', align: 'center', sortable: true, value: 'Ru' },
    { text: 'Actions', align: 'center', sortable: true, value: 'actions' }
  ]

  config: { showLink: boolean; plotlyServerURL: string } = {
    showLink: true,
    plotlyServerURL: 'https://chart-studio.plotly.com'
  }

  mounted() {
    this.dataSets = this.rawData
    this.updateImgOffcet()
    this.renderReact()
  }

  @Watch('imgXpoint')
  onImgXpointChanged() {
    this.renderReact()
  }

  @Watch('imgYpoint')
  onImgYpointChanged() {
    this.renderReact()
  }

  @Watch('imgSize')
  onImgSizeChanged() {
    this.renderReact()
  }

  @Watch('imgOpacity')
  onImgOpacityChanged() {
    this.renderReact()
  }

  @Watch('imgSelect')
  onImgSelectChanged() {
    this.updateImgOffcet()
    this.renderReact()
  }

  @Watch('dataSets', { deep: true })
  onDataSets() {
    this.renderReact()
  }

  updateImgOffcet() {
    this.imgXpoint = this.imgSelect.x
    this.imgYpoint = this.imgSelect.y
    this.imgSize = this.imgSelect.size
  }

  get formTitle(): string {
    return this.editedIndex === -1 ? 'New Item' : 'Edit Item'
  }

  renderReact() {
    Plotly.react(
      this.$refs.ternaly,
      [
        {
          type: 'scatterternary',
          mode: 'markers',
          a: this.rawData.map(function(d) {
            return d.Al
          }),
          b: this.rawData.map(function(d) {
            return d.Ru
          }),
          c: this.rawData.map(function(d) {
            return d.Pd
          }),
          text: this.rawData.map(function(d) {
            return d.label
          }),
          hovertemplate:
            '%{text}<br>Al: %{a:.2f}<br>Pd: %{b:.2f}<br>Ru: %{c:.2f}',
          marker: {
            symbol: 100,
            color: '#DB7365',
            size: 2,
            line: { width: 1 }
          }
        }
      ],
      {
        ternary: {
          sum: 100,
          aaxis: this.makeAxis('Al', 0, 50),
          baxis: this.makeAxis('Ru', 45, 0),
          caxis: this.makeAxis('Pd', -45, 0),
          bgcolor: 'rgba(0,0,0,0)'
        },
        annotations: [
          {
            showarrow: false,
            text: this.imgSelect.title,
            x: 0.5,
            y: 1.3,
            font: { size: 15 }
          }
        ],
        paper_bgcolor: 'rgba(0,0,0,0)',
        images: [
          {
            x: this.imgXpoint,
            y: this.imgYpoint,
            sizex: this.imgSize,
            sizey: this.imgSize,
            source: this.imgSelect.src,
            opacity: this.imgOpacity,
            layer: 'below'
          }
        ]
      },
      this.config
    )
  }

  makeAxis(title: string, tickangle: number, min: number) {
    return {
      title,
      titlefont: { size: 20 },
      min,
      tickangle,
      tickfont: { size: 15 },
      tickcolor: 'rgba(0,0,0,0)',
      ticklen: 5,
      showline: true,
      showgrid: true
    }
  }

  editItem(item: any) {
    this.editedIndex = this.dataSets.indexOf(item)
    this.editedItem = Object.assign({}, item)
    this.dialog = true
  }

  deleteItem(item: any) {
    const index = this.dataSets.indexOf(item)
    confirm('Are you sure you want to delete this item?') &&
      this.dataSets.splice(index, 1)
  }

  close() {
    this.dialog = false
    this.$nextTick(() => {
      this.editedItem = Object.assign({}, this.defaultItem)
      this.editedIndex = -1
    })
  }

  save() {
    if (this.editedIndex > -1) {
      Object.assign(this.dataSets[this.editedIndex], this.editedItem)
    } else {
      this.dataSets.push(this.editedItem)
    }
    this.close()
  }
}
</script>
